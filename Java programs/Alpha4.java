class Alpha4
{
    public static void main(String[] args) 
    {
        System.out.println("Printing all uppercase letters:");
        for (char c = 'A'; c <= 'Z'; c++) 
        {
            System.out.print(c + " ");
        }
        System.out.println("\n\nPrinting digits 0 to 9:");
        for (char d = '0'; d <= '9'; d++) 
        {
            System.out.print(d + " ");
        }
    }
}